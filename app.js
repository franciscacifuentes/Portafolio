/*
    Aprendiste a destacar el texto utilizando la etiqueta <strong>, sin embargo,
    hay otra etiqueta que también se utiliza mucho para esto, que es la etiqueta <span>. 
    A diferencia de <strong>, <span> no aplica negrita por defecto, pero es una excelente 
    forma de resaltar partes del texto en HTML. Mira:

    HTML
    <h1>Formación de <span>Front-end</span></h1>

    CSS

    h1{
        font-weight: bold;
    }

    span{
        color: #22D4FD;
        border: 1px solid #22D4FD;
        padding: 10px;
    }
 */

/*
    como crear clases en css y html
    primero en html por ejemplo quiero una clase en <strong></strong>
    debo agregar asi: <strong class="tituloPrincipal">con un Front-end de calidad!</strong>
    y en css llamar SIEMPRE con un punto:

    .tituloPrincipal{
        color: #22D4FD;
    }
    
    Se utilizan las clases para diferencia mismas etiquetas, el ejemplo de arriba ya teniamos otro strong y 
    por eso introducimos una clase para dar color a cada strong
*/

